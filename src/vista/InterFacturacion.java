    //metodo para restar stock
    private void RestarStockProductos(int idProducto, int cantidad) {

        int cantidadProductoBaseDatos = 0;
        try {

            Connection cn = Conexion.conectar();
            String sql = "SELECT idProducto, cantidad FROM tb_producto WHERE idProducto = '" + idProducto + "'";
            Statement st;
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {

                cantidadProductoBaseDatos = rs.getInt("cantidad");

            }
            cn.close();

        } catch (SQLException e) {

            System.out.println("Error al obtener id de stock" + e);
        }

        try {

            Connection cn = Conexion.conectar();
            PreparedStatement consulta = cn.prepareStatement("UPDATE tb_producto set cantidad=? WHERE idProducto='" + idProducto + "'");
            int cantidadNueva = cantidadProductoBaseDatos - cantidad;
            consulta.setInt(1, cantidadNueva);
            if (consulta.executeUpdate() > 0) {

                System.out.println("Actualizacoon de stock correcta");

            }

            cn.close();

        } catch (SQLException e) {

            System.out.println("Error al actualizar stock" + e);

        }

    }

