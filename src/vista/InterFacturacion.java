    private void jButton_buscar_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_buscar_clienteActionPerformed

        String clienteBuscar = txt_cliente_buscar.getText().trim();
        Connection cn = Conexion.conectar();

        String sql = "SELECT nombre, apellido FROM tb_cliente WHERE cedula='" + clienteBuscar + "'";
        Statement st;

        try {

            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {

                jComboBox_cliente.setSelectedItem(rs.getString("nombre") + " " + rs.getString("apellido"));

            } else {

                jComboBox_cliente.setSelectedItem("Seleccione cliente:");
                JOptionPane.showMessageDialog(null, "DNI de cliente incorrecta o no encontrada");

            }
            txt_cliente_buscar.setText("");
            cn.close();

        } catch (SQLException e) {

            System.out.println("Error al buscar cliente, " + e);
        }


    }//GEN-LAST:event_jButton_buscar_clienteActionPerformed

    private void jButton_añadir_productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_añadir_productoActionPerformed

        String combo = this.jComboBox_producto.getSelectedItem().toString();
        //validar que se selecciono producto
        if (combo.equalsIgnoreCase("Seleccione producto:")) {

            JOptionPane.showMessageDialog(null, "Seleccione un producto");

        } else {

            //validamos que haya ingresado una cantida
            if (!txt_cantidad.getText().isEmpty()) {

                //validamos que el usuario no ingrese caracteres no numericos en cantidad
                boolean validacion = validar(txt_cantidad.getText());

                //validamos la respuesta de validar
                if (validacion) {

                    //validamos que cantidad sea mayor a 0
                    if (Integer.parseInt(txt_cantidad.getText()) > 0) {

                        cantidad = Integer.parseInt(txt_cantidad.getText());
                        //metodo para obtener los datos del producto
                        this.DatosDelProducto();

                        //validamos la cantidad de productos no sea mayor al stock
                        if (cantidad <= cantidadProductoBBDD) {

                            subtotal = precioUnitario * cantidad;
                            totalpagar = subtotal + iva + descuento;

                            //redondeamos decimales
                            subtotal = (double) Math.round(subtotal * 100) / 100;
                            iva = (double) Math.round(iva * 100) / 100;
                            descuento = (double) Math.round(descuento * 100) / 100;
                            totalpagar = (double) Math.round(totalpagar * 100) / 100;

                            //creamos un nuevo producto
                            producto = new DetalleVenta(auxIdDetalle,
                                    1,
                                    idProducto,
                                    nombre,
                                    Integer.parseInt(txt_cantidad.getText()),
                                    precioUnitario,
                                    subtotal,
                                    descuento,
                                    iva,
                                    totalpagar,
                                    1//estado

                            );

                            //añadimos el producto a la lista
                            listaProductos.add(producto);

                            JOptionPane.showMessageDialog(null, "Producto agregado");
                            auxIdDetalle++;
                            txt_cantidad.setText("");
                            //volvemos a cargar el combo de productos
                            this.CargarComboProductos();
                            //metodo para calcular
                            this.CalcularTotalPagar();
                            //habilitamos los botones para guardar
                            txt_efectivo.setEnabled(true);
                            jButton_calcular_cambio.setEnabled(true);

                        } else {

                            JOptionPane.showMessageDialog(null, "La cantidad seleccionada supera el stock");

                        }

                    } else {

                        JOptionPane.showMessageDialog(null, "La cantidad no puede ser 0 o negativo");
                    }

                } else {

                    JOptionPane.showMessageDialog(null, "Ingrese solo valores numericos en cantidad");

                }

            } else {

                JOptionPane.showMessageDialog(null, "Ingrese una cantidad");
            }

        }
        //cargamos el metodo de productos agregados

        this.listaTablaProductos();

    }//GEN-LAST:event_jButton_añadir_productoActionPerformed

    private void jButton_calcular_cambioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_calcular_cambioActionPerformed

        if (!txt_efectivo.getText().isEmpty()) {

            boolean validacion = validarDouble(txt_efectivo.getText());
            //validamos que solo ingrese caracteres numericos
            if (validacion == true) {

                double dinero = Double.parseDouble(txt_efectivo.getText().trim());
                double totalDinero = Double.parseDouble(txt_total_pagar.getText().trim());
                //validamos que el monto sea positivo
                if (dinero < totalDinero) {

                    JOptionPane.showMessageDialog(null, "Dinero ingresado es insuficiente");

                } else {

                    double cambio = (dinero - totalDinero);
                    double camb = (double) Math.round(cambio * 100) / 100;
                    String cam = String.valueOf(camb);
                    txt_cambio.setText(cam);
                }

            } else {

                JOptionPane.showMessageDialog(null, "Ingrese el monto en caracteres solo numericos");
            }

        } else {

            JOptionPane.showMessageDialog(null, "Ingrese un monto de efectivo para calcular el cambio");

        }

    }//GEN-LAST:event_jButton_calcular_cambioActionPerformed

